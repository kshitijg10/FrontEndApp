package com.kshitij.web;

public class User {

	private String user;
	private String password;
	public String getUser() {
		return user;
	}
	public void setUser(String login) {
		this.user = login;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
}
